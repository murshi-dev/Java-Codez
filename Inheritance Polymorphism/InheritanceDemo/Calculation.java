
public class Calculation {
	protected int num1;
	protected int num2;
	Calculation()
	{
		num1=0;
		num2=0;
	}
	//setter
	public void setNum1(int n1)
	{
		num1=n1;
	}
	public void setNum2(int n2)
	{
		num2=n2;
	}
	//getter
	public int getNum1()
	{
		return num1;
	}
	public int getNum2()
	{
		return num2;
	}


}
